// 만약, 짝수와 홀수의 합을 각각 별도로 누적할 경우
let evenTotal = 0; // 짝수 누적 변수
let oddTotal = 0; // 홀수 누적 변수

for(let i = 1; i <= 10; i++) {
	if(i % 2 == 0) { // 짝수
		// 짝수값을 evenTotal 변수에 누적
		evenTotal += i;
	} else if(i % 2 == 1) { // 홀수
		// 짝수값을 evenTotal 변수에 누적
		oddTotal += i;
	}
}

document.write(`1 ~ 10 까지의 짝수의 합 : ${evenTotal}<br>`);
document.write(`1 ~ 10 까지의 홀수의 합 : ${oddTotal}<br>`);
























