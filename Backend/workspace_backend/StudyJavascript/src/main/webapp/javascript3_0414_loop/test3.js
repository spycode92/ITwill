// for 문을 사용하여 위의 작업과 동일하게 1 ~ 10 까지의 합 계산하기
// 1) 반복문에서 덧셈을 누적하여 저장할 변수 선언하고 초기화
let sum = 0; // 초기값을 0으로 설정
// => 주의! 반드시 for문보다 먼저(for문 바깥쪽 윗쪽에서) 누적 변수 선언 및 초기화

// 2) 합계 계산 대상 값인 1 ~ 10 까지 1씩 증가하는 반복문 생성
for(let i = 1; i <= 10; i++) {
	// 주의! sum = 0(초기화) 코드가 현재 반복문 내로 이동할 경우
	// 반복할 때마다 0 으로 초기화되므로 정상적인 누적이 불가능하다!
	
	// 3) 변수 sum 에 i값 덧셈을 누적
	//    => sum + i 결과를 다시 변수 sum 에 저장
//	sum = sum + i;
	sum += i;
	
	// 만약, sum 값을 for문 내에서 출력할 경우 연산 과정(누적 과정)을 확인할 수 있다!
	document.write(`1 ~ 10 까지의 합(for) : ${sum}<br>`);
} // for 문 끝

// 4) 합계 계산 결과(sum) 출력
// => 최종적으로 계산 결과만 출력할 경우 for문 종료 후 출력
document.write(`1 ~ 10 까지의 합(for) : ${sum}<br>`);

