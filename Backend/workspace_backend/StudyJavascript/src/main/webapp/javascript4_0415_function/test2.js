// 1. "Hello, World!" 문자열을 리턴하는 함수 getMessage() 정의
function getMessage() {
	document.write(`getMessage() 함수 호출됨!<br>`);
	
	// 함수 내에서 리턴값이 있을 경우 return 문을 명시하고
	// return 문 뒤에 리턴할 데이터를 값 또는 변수 등의 형태로 지정(연산도 가능함)
	return "Hello, World!"; // 문자열 "Hello, World!"를 함수 호출한 곳으로 리턴(되돌아갈 때 전달함)
	// => 함수 내에서 return 문이 실행되면 현재 실행중인 함수를 "즉시" 종료하고
	//    함수를 호출한 원래 위치로 돌아감(이 때, return 값이 있으면 그 값을 가지고 돌아감)
	// => 따라서, return 문보다 아래쪽의 문장들은 실행되지 못한다!
	document.write(`getMessage() 함수 종료됨!<br>`); // 실행되지 않는 문장(Unreachable code)
}
