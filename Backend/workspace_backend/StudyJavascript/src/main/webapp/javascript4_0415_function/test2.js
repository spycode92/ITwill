// => 전달인자(함수 호출 시 전달하는 데이터)가 없으므로, 함수에서 전달받을 데이터도 없음 
//    따라서, 함수 정의 시 함수명 뒤의 소괄호() 부분에 아무것도 기술하지 않음
// => 리턴값이 있으므로 함수 작업 종료 시 호출한 곳으로 되돌려줄 데이터를 return 문 뒤에 기술
//    기본 문법 : return 리턴값;
